import { db } from 'src/lib/db'

export const users = () => {
  return db.user.findMany()
}

export const user = ({ id }) => {
  return db.user.findUnique({
    where: { id },
  })
}

export const createUser = ({ input }) => {
  return db.user.create({
    data: input,
  })
}

export const User = {
  bcard: (_obj, { root }) =>
    db.user.findUnique({ where: { id: root.id } }).bcard(),
}
